#include "main.h"
#include <string.h>

// VCP
UART_HandleTypeDef huart2;
I2C_HandleTypeDef hi2c1;

void SystemClock_Config(void);
static void MX_GPIO_Init(void);
static void MX_I2C1_Init(void);
static void MX_USART2_UART_Init(void);

// Sensiron SFM4100 address is actually 0x01 shifted to the left by 1 bit
#define SENSORON_ADDR		(0x01 << 1)
// Commands
uint8_t flow = 0xf1;
uint8_t temperature = 0xf3;
uint8_t voltage = 0xf5;
uint8_t adv_user_reg_r = 0xe5;

// Receive buffer
uint8_t receive_buffer[4] = {0};

int main(void)
{
  HAL_Init();
  SystemClock_Config();
  MX_GPIO_Init();
  MX_I2C1_Init();
  MX_USART2_UART_Init();

  while (1)
  {
	  HAL_Delay(250);
	  HAL_GPIO_TogglePin(LD3_GPIO_Port, LD3_Pin);

	  // Get advanced user register. Data as of 5 Sep is 0x76C2
	  HAL_I2C_Master_Transmit(&hi2c1, SENSORON_ADDR, &adv_user_reg_r, 0x01, 100);
	  HAL_I2C_Master_Receive(&hi2c1, SENSORON_ADDR, receive_buffer, 0x04, 100);

	  // Trigger flow measurement. Resolution: 12 bits
	  HAL_I2C_Master_Transmit(&hi2c1, SENSORON_ADDR, &flow, 0x01, 100);
	  HAL_I2C_Master_Receive(&hi2c1, SENSORON_ADDR, receive_buffer, 0x04, 100);

	  // Trigger temperature measurement. Resolution: 9 bits
	  HAL_I2C_Master_Transmit(&hi2c1, SENSORON_ADDR, &temperature, 0x01, 100);
	  HAL_I2C_Master_Receive(&hi2c1, SENSORON_ADDR, receive_buffer, 0x04, 100);

	  // Trigger voltage measurement - not implemented on sensor
	  HAL_I2C_Master_Transmit(&hi2c1, SENSORON_ADDR, &voltage, 0x01, 100);
	  HAL_I2C_Master_Receive(&hi2c1, SENSORON_ADDR, receive_buffer, 0x04, 100);

	  // @TODO Read serial number

  }

}

